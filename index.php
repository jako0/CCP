	<h1>Presentan estudio para evaluar relevancia de Pilar Solidario</h1>
	<ul>
           <li>Dados los tiempos estipulados para su desarrollo, los resultados del estudio estaran disponibles en el mes de abril de 2017.</li>
	</ul>
           <img src="noticias/2016-12-06-evaluacion-pilar-solidario.jpg" alt="Evaluacion del Pilar Solidario"/>
	<p>El pasado miércoles 16 de noviembre, se realizó la primera reunión de trabajo del estudio licitado en conjunto por el Consejo Consultivo Previsional (CCP) y la Comisión de Usuarios del Sistema Previsional (CU), a través de la Subsecretaría de Prevision Social, el cual fue adjudicado por el Centro de Encuestas y Estudios Longitudinales de la Universidad Catolica (CEEL-UC).</p><p>El estudio se titula "Evaluacion del Pilar Solidario en el Financiamiento de la Canasta de Consumo, los Ingresos y la Pobreza Multidimensional de Hombres y Mujeres Adultos Mayores" y su objetivo general es caracterizar cuantitativa y cualitativamente la canasta de consumo, fuentes de ingresos e indicadores de pobreza multidimensional, para mujeres y hombres adultos mayores en diferentes tipologias de familias, de modo de dimensionar la relevancia del Pilar Solidario.</p><p>En la reunion, el equipo del CEEL-UC, liderado por Patricia Medrano, presento la planificacion del estudio y posteriormente se realizaron los comentarios por parte de los consejeros, tanto del CCP como de la CU, con el fin de ajustar los detalles pertinentes para el buen desarrollo.</p>
